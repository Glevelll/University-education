public class Calculator {

    public static double sum(double a, double b){
        return a + b;
    }
    public static double diff(double a, double b){
        return a - b;
    }
    public static double mult(double a, double b){
        return a * b;
    }
    public static double div(double a, double b){
        return a / b;
    }
}
